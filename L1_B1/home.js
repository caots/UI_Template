let checkCountUp = false;
window.addEventListener("scroll", function () {

    var x = pageYOffset;
    if (x >= 5750 && !checkCountUp) {
        countUpLoad();
        checkCountUp = true
    }
})

function countUpLoad() {
    var options = {
        useEasing: true,
        useGrouping: true,
        separator: ',',
        decimal: '.',
        prefix: '',
        suffix: ',000'
    };
    var counter1 = new CountUp("counter-1", 0, 300, 0, 2.5, options);
    counter1.start();
    var counter2 = new CountUp("counter-2", 0, 100, 0, 2.5, options);
    counter2.start();
    var counter3 = new CountUp("counter-3", 0, 50, 0, 2.5, options);
    counter3.start();

}